def Kraus_rep(X: np.ndarray, d: int, pdim: int, rK: int) -> np.ndarray:
    """Compute the Kraus representations for all gates in the gate set.

    Args:
        X: Current gate estimate
        d: Number of gates
        pdim: Physical dimension
        rK: Target Kraus rank

    Returns:
        K: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.

    Notes:
        The Kraus representation is obtained from a singular value decomposition
        of the Choi matrix. If parameter rK is smaller than the true rank of the
        Choi matrix, a rank rK approximation is used. Approximations are only CPT
        in the special case where the original gate was already of rank <= rK.

    """
    X_choi = X.reshape(d, pdim, pdim, pdim, pdim)
    X_choi = np.einsum("ijklm->iljmk", X_choi).reshape((d, pdim**2, pdim**2))
    K = np.zeros((d, rK, pdim, pdim)).astype(np.complex128)
    psd_tolerance = 1e-12
    for i in range(d):
        w, v = la.eigh(X_choi[i])
        if np.min(w) < -psd_tolerance:
            raise ValueError(f"Choi Matrix is not positive definite within tolerance {psd_tolerance}")
        K[i] = np.einsum(
            "ijk->kji",
            (v[:, -rK:] @ np.diag(np.sqrt(np.abs(w[-rK:])))).reshape(pdim, pdim, rK),
        )
        # Trace normalization of Choi Matrix
        K[i] = K[i] / np.sqrt(np.einsum("ijk,ijk", K[i], K[i].conj())) * np.sqrt(pdim)
    return np.array(K)


def sampled_measurements(y: np.ndarray, n_samples: int) -> np.ndarray:
    """Compute finite sample estimates of input probabilities.

    Args:
        y: 2D array of measurement outcomes for different sequences, Each column contains the outcome probabilities for
            a fixed sequence
        n_samples: Number of samples for each experiment

    Returns:
        y_sampled: 2D array of sampled measurement outcomes for different sequences;
            Each column contains the sampled outcome probabilities for a fixed sequence

    Notes:
        The entries of each column of y form a probability distribution.
        From this distribution n random samples are drawn which give estimates for
        the initial probabilities. This simulates finite sample size data.

    """
    n_povm, m = y.shape
    cap_tolerance = 1e-6
    if any(y.reshape(-1) > 1) or any(y.reshape(-1) < 0):
        y_new = np.maximum(np.minimum(y, 1), 0)
        if np.sum(np.abs(y_new - y)) > cap_tolerance:
            warnings.warn(
                f"Warning: Probabilities capped to interval [0,1], l1-difference to input: {np.sum(np.abs(y_new - y))}",
                stacklevel=2,
            )
        y = y_new
    rng = np.random.default_rng()
    y_sampled = np.zeros(y.shape)
    for i in range(m):
        y_sampled[:, i] = rng.multinomial(n_samples, [y[o, i] for o in range(n_povm)]) / n_samples
    return y_sampled


def random_len_seq(d: int, min_l: int, max_l: int, N: int) -> np.ndarray:
    """Generate random gate sequence instructions.

    These contain sequences of different lengths,
    the lengths are drawn uniformly at random from (min_l, ..., max_l).

    Args:
        d: Number of gates
        min_l: Minimum sequence length
        max_l: Maximum sequence length
        N: Number of random sequences

    Returns:
        J: 2D array where each row contains the gate indices of a gate sequence

    """
    seq_lengths = np.random.randint(min_l, max_l + 1, N)
    J = []
    for length in seq_lengths:
        j_curr = np.random.randint(0, d, length)
        J.append(list(np.pad(j_curr, (0, max_l - length), "constant", constant_values=-1)))
    return np.array(J)


def generate_fids(d: int, length: int, m_f: int) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Generate random fiducial sequencecs.

    Args:
        d: Number of gates
        length: Total sequence length
        m_f: Number of random fiducial sequences

    Returns:
        J_fid: Sequence list of only the fiducial sequences
        J_fid2: Sequence list for all combinations of two concatenated fiducial sequences
        J_meas: Sequence list for all combinations of fiducials seuqneces with a gate in between:
            fiducial1 -- gate -- fiducial2

    """
    fid_length = (length - 1) // 2
    fid = random.sample(range(d**fid_length), m_f).copy()
    J_fid = [list(local_basis(ind, d, fid_length)) for ind in fid]
    J_fid2 = np.array([seqL + seqR for seqL in J_fid for seqR in J_fid])
    J_meas = np.zeros((d, m_f**2, length), dtype="int")
    for k in range(d):
        J_meas[k] = np.array([seqL + [k] + seqR for seqL in J_fid for seqR in J_fid])
    return np.array(J_fid), J_fid2, J_meas


def tvd(X: np.ndarray, E: np.ndarray, rho: np.ndarray, J: np.ndarray, y_data: np.ndarray) -> float:
    """Return the total variation distance between model probabilities and y_data.

    Args:
        X: Gate set
        E: POVM
        rho: Initial state
        J: 2D array where each row contains the gate indices of a gate sequence
        y_data: 2D array of measurement outcomes for sequences in J;
            Each column contains the outcome probabilities for a fixed sequence

    Returns:
        dist: The total variation distance.

    """
    n_povm = y_data.shape[0]
    y_model = np.real(np.array([[E[i].conj() @ contract(X, j) @ rho for j in J] for i in range(n_povm)]))
    dist = float(la.norm(y_model - y_data, ord=1)) / 2
    return dist


def random_seq_design(d: int, l_min: int, l_cut: int, l_max: int, N_short: int, N_long: int) -> np.ndarray:
    """Generate a set of random sequences ith given lengths.

    Disclaimer: Draws without replacement but inefficiently (not working for sequence length > 24)
    This sequence lengths (circuit depths) are chosen for a mix of very short sequences (better convergence) and some
    slightly longer sequences to reduce the generalization error.
    This sequence design is heuristic and intended for coarse and fast estimates. For very accurate estimates at the
    cost of higher measurement effort it is recommended to use pyGSTi with long sequence GST.

    Args:
        d: The number of gates in the gate set
        l_min: Minimum sequence lenght
        l_cut: Cutoff sequence lenght: N_short sequences are equally distributed among lengths l_min < l < l_cut
        l_max: N_long sequences are equally distributed among lengths l_cut + 1 < l < l_max. Currently l_max < 24 only.
        N_short: Number of short sequences
        N_long: Number of long sequences

    Returns:
        J: 2D array where each row contains the gate indices of a gate sequence

    """
    # Open problems:
    # - Change randomness to work with longer sequences;
    # - Handle case where Number of sequences is smaller than the available lengths

    max_supported = 23
    if l_max > max_supported:
        raise ValueError(f"Currently only sequences lenghts <= {max_supported} are supported.")

    J = np.array([-np.ones(l_max)])
    # Short sequences:
    seq_counts = []
    N_remaining = N_short
    for length in range(l_min, l_cut + 1):
        seq_counts.append(int(np.min([np.floor(N_remaining / (l_cut + 1 - length)), d**length])))
        if seq_counts[-1] > 0:
            ind_curr = np.array(random.sample(range(d**length), seq_counts[-1]))
            J_curr = np.array(
                [
                    np.pad(
                        local_basis(ind, d, length),
                        (0, l_max - length),
                        "constant",
                        constant_values=-1,
                    )
                    for ind in ind_curr
                ]
            )
            J = np.concatenate((J, J_curr), axis=0)
            N_remaining = N_short - 1 - np.sum(seq_counts)
    if N_remaining > 0:
        print(
            "Number of possible sequences without replacement for the given sequence\
        length range is lower than the desired total number of sequences"
        )

    # Long sequences:
    seq_counts = []
    N_remaining = N_long
    for length in range(l_cut + 1, l_max + 1):
        seq_counts.append(int(np.min([np.floor(N_remaining / (l_max + 1 - length)), d**length])))
        if seq_counts[-1] > 0:
            ind_curr = np.array(random.sample(range(d**length), seq_counts[-1]))
            J_curr = np.array(
                [
                    np.pad(
                        local_basis(ind, d, length),
                        (0, l_max - length),
                        "constant",
                        constant_values=-1,
                    )
                    for ind in ind_curr
                ]
            )
            J = np.concatenate((J, J_curr), axis=0)
            N_remaining = N_long - np.sum(seq_counts)
    return J.astype(int)


def n_params(pdim: int, d: int, rK: int, n_povm: int) -> int:
    """Returns the number of free parameters in a gate set.

    Unitary gauge parameters and unitary freedom in representing the Kraus operators and the POVM elements are
    subtracted. For details, see
    https://docserv.uni-duesseldorf.de/servlets/DerivateServlet/Derivate-71735/dissertation_brieger-2%281%29.pdf

    Args:
        pdim: The physical dimension, i.e. 2^(#qubits)
        d: The number of gates in the gate set
        rK: The Kraus rank of the gates
        n_povm: The number of POVM elements; For computational basis measurements n_povm = pdim

    Returns:
        n_params: The number of free parameters

    """
    # Order: gates + stat + povm - povm_freedom - gauge freedom - Kraus freedom
    return d * (pdim**2 * (2 * rK - 1) - rK**2) + pdim * (n_povm * pdim - pdim)


def multikron(matrix_array: np.ndarray) -> np.ndarray:
    """Computes the Kronecker product of all matrices in an array.

    The order is matrix_array[0] otimes matrix_array[1] otimes ...

    Args:
        matrix_array: An array containing matrices

    Returns:
        res: The resulting tensor product (potentially a very large array)

    """
    res = matrix_array[0]
    for i in range(1, matrix_array.shape[0]):
        res = np.kron(res, matrix_array[i])
    return res


def local_dephasing_pp(prob_vec: list[float]) -> np.ndarray:
    """Returns the tensor product of single qubit dephasing channels in Pauli basis.

    Args:
        prob_vec: A list of dephasing probabilities

    Returns:
        D_final: Process matrix of the tensor product of local dephasing channels

    """
    D_loc_array = np.array([[[1, 0, 0, 0], [0, 1 - 2 * p, 0, 0], [0, 0, 1 - 2 * p, 0], [0, 0, 0, 1]] for p in prob_vec])
    D_final = multikron(D_loc_array)
    return D_final


def outcome_probs_from_files(
    folder_name: str, basis_dict: dict[str, int], n_povm: int, N: int
) -> tuple[np.ndarray, int]:
    """Searches a specified folder for .txt files containing circuit outcomes and combines the results.

    Each text file needs to have line in the following format:
    1: 1,0,1,0,1,1,0,0,0
    2: 1,1,1,1,0,1,1,1,1
    Here the first number specifies the circuit and basis outcome of each shot is written to the right.

    Args:
        folder_name: The relative or absolute name of the data folder
        basis_dict: Translation between label for each shot in the .txt files and the numbering of the POVM element.
            Example (two qubits): {"00": 0, "01": 1, "10": 2, "11": 3,}
        n_povm: The number of POVM elements in the data set
        N: The number of circuits in the data set

    Returns:
        y: 2D array of measurement outcomes for all measured sequences; Each column contains the outcome probabilities
            for a fixed sequence
        avg_counts: The average number of shots per circuit after combining data sets

    """
    filenames = os.listdir(path=folder_name)
    datafile_names = [s for s in filenames if ".txt" in s]

    # array of outcome probabilities:
    y = np.zeros((n_povm, N))
    sample_counts = np.zeros((len(datafile_names), N))
    k = 0
    for filename in datafile_names:
        with open(folder_name + "/" + filename, encoding="ASCII") as file:
            i = 0
            for line in file:
                # removing row index at beginning and \n mark at end
                line_entries = line.rstrip().split(": ")
                # splitting result string at commas
                result_list = line_entries[1:][0].split(",")
                # translate each measurement result onto basis index
                for entry in result_list:
                    j = basis_dict[entry]
                    y[j, i] += 1
                sample_counts[k, i] = len(result_list)
                i += 1
        k += 1
    total_counts = np.sum(sample_counts, axis=0)
    avg_counts = int(np.average(total_counts))
    return y / total_counts, avg_counts
