def transp(dim1: int, dim2: int) -> np.ndarray:
    """Superoperator of a map that performs the transpose operation.

    Args:
        dim1: First dimension of the matrix to be transposed
        dim2: Second dimension of the matrix to be transposed

    Returns:
        T: Transpose superoperator that turns a dim1xdim2 matrix into a dim2xdim1 matrix

    Notes:
        The projection is done with respect to the canonical metrik.

    """
    id1 = np.eye(dim1)
    id2 = np.eye(dim2)
    T = np.einsum("il,jk->ijkl", id2, id1).reshape(dim1 * dim2, dim1 * dim2)
    return T


def randpsd(n: int, normalized: str = "True") -> np.ndarray:
    """Generate a random positive semidefinite square matrix.

    Args:
        n: Number of matrix entries
        normalized: Controls if the output is trace normalized

    Returns:
        mat: Random positive definite square matrix

    Notes:
        Eigenvalues are drawn uniformly from the interval [0,1);
        The basis is generated by diagonalizing a random hermitian matrix (see randHerm function).

    """
    dim = int(np.sqrt(n))
    H = randHerm(dim)
    U, _, _ = np.linalg.svd(H)
    evals = np.random.random_sample(dim)
    if normalized == "True":
        evals /= np.sum(evals)
    mat = np.dot(np.dot(U, np.diag(evals)), U.T.conj())
    mat = mat.reshape(-1)
    return mat


def randvec(n: int) -> np.ndarray:
    """Generate vector with real and imaginary part drawn from the normal distribution.

    Args:
        n: Number of elements for the random vector

    Returns:
        g: Length n vector with complex entries whose real and imaginary part is independently drawn
            from the normal distribution with mean 0 and variance 1.

    """
    # randn(n) produces a random vector of length n with mean 0 and variance 1
    g = np.random.randn(n) + 1j * np.random.randn(n)
    g = g / np.linalg.norm(g)
    return g


def randHerm(n: int) -> np.ndarray:
    """Generate random square hermitian matrix.

    Args:
        n: Matrix dimension

    Returns:
        G: Random hermitian matrix normalized in spectral norm

    Notes:
        First a matrix with random complex entries is generated (see randvec function).
        This matrix is then projected onto the space of hermitian
        matrices and normalized in spectral norm.

    """
    G = randvec(n * n).reshape(n, n)
    G = (G + G.T.conj()) / 2
    # ord=2 gives the spectral norm
    G = G / np.linalg.norm(G, ord=2)
    return G


def randHermGS(d: int, r: int) -> np.ndarray:
    """Generates random set of operators that are hermiticity preserving.

    Args:
        d: Number of gates
        r: Superoperator dimension of the gates given by the square of the physical dimension

    Returns:
        X: Array where random hermiticity preserving operators are stacked along the first axis.

    Notes:
        The function randHerm is used to generate random hermitian Choi matrices,
        whose indies are then rearanged to obtain random hermiticity preserving superoperators.

    """
    dim = int(np.sqrt(r))
    X = np.zeros((r, d, r), dtype="complex")
    for i in range(d):
        H = randHerm(r).reshape(dim, dim, dim, dim)
        H = np.einsum("ijkl->jlik", H)
        X[:, i, :] = H.reshape(r, r)
    return X


def randU(n: int, a: float = 1) -> np.ndarray:
    """Generates random unitary from a random hermitian generator.

    Args:
        n: Matrix dimension of the unitary
        a: Parameter to control the norm of the hermitian generator

    Returns:
        U: Matrix exponential of random hermitian matrix times the imaginary unit.

    """
    return expm(1j * a * randHerm(n)).astype(np.complex128)


def randU_Haar(n: int) -> np.ndarray:
    """Return a Haar distributed random unitary.

    Args:
        n: Matrix dimension of the unitary

    Returns:
        U: Random unitary matrix distributed according to the Haar measure.

    """
    Z = np.random.randn(n, n) + 1.0j * np.random.randn(n, n)
    [Q, R] = qr(Z)
    D = np.diag(np.diagonal(R) / np.abs(np.diagonal(R)))
    return np.dot(Q, D)


def randKrausSet(d: int, r: int, rK: int, a: float = 1) -> np.ndarray:
    """Generates random set of Kraus operators.

    Args:
        d: Number of gates
        r: Superoperator dimension of the gates given by the square of the physical dimension
        rK: Number of Kraus operators per gate ("Kraus rank")
        a: Parameter to control the norm of the hermitian generator and thereby how far the gates are from the identity

    Returns:
        K: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.

    Notes:
        Let pdim be the physical dimension. Then a set of Kraus operators is generated by taking the first pdim
            columns of a random unitary of size rK*pdim. The random unitary is generated from a random hermitian matrix.

    """
    pdim = int(np.sqrt(r))
    K = np.zeros((d, rK, pdim, pdim)).astype(np.complex128)
    for i in range(d):
        K[i, :, :, :] += randU(pdim * rK, a)[:, :pdim].reshape(rK, pdim, pdim)
    return K


def randKrausSet_Haar(d: int, r: int, rK: int) -> np.ndarray:
    """Generates random set of Kraus operators.

    Args:
        d: Number of gates
        r: Superoperator dimension of the gates given by the square of the physical dimension
        rK: Number of Kraus operators per gate ("Kraus rank")

    Returns:
        K: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.

    Notes:
        Let pdim be the physical dimension. Then a set of Kraus operators is generated by taking the first
            pdim columns of a random unitary of size rK*pdim. The random unitary is generated according to
            the Haar measure.

    """
    pdim = int(np.sqrt(r))
    K = np.zeros((d, rK, pdim, pdim)).astype(np.complex128)
    for i in range(d):
        K[i, :, :, :] += randU_Haar(pdim * rK)[:, :pdim].reshape(rK, pdim, pdim)
    return K


def random_gs(d: int, r: int, rK: int, n_povm: int) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """Generates a random gate using the Gaussian unitary ensemble, initial state and POVM.

    Args:
        d: Number of gates
        r: Superoperator dimension of the gates given by the square of the physical dimension
        rK: Number of Kraus operators per gate ("Kraus rank")
        n_povm: Number of POVM-Elements

    Returns:
        K: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.
        X: Array where random CPT superoperators are stacked along the first axis.
        E: Randomly generated POVM elements stacked along the first axis.
        rho: Random initial state vector

    Notes:
        The Kraus operators are generated from random unitaries, see function randKrausSet

    """
    K = randKrausSet(d, r, rK).copy()
    X = np.einsum("ijkl,ijnm -> iknlm", K, K.conj()).reshape((d, r, r))
    rho = randpsd(r).copy()
    A = randKrausSet(1, r, n_povm)[0].conj()
    E = np.array([(A[i].T.conj() @ A[i]).reshape(-1) for i in range(n_povm)]).copy()
    return K, X, E, rho


def random_gs_Haar(d: int, r: int, rK: int, n_povm: int) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    """Generates a random gate set with gates from Haar random unitaries, initial state and POVM.

    Args:
        d: Number of gates
        r: Superoperator dimension of the gates given by the square of the physical dimension
        rK: Number of Kraus operators per gate ("Kraus rank")
        n_povm: Number of POVM-Elements

    Returns:
        K: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.
        X: Array where random CPT superoperators are stacked along the first axis.

    Notes:
        The Kraus operators are generated from Haar random unitaries, see function randKrausSet_Haar

    """
    K = randKrausSet_Haar(d, r, rK).copy()
    X = np.einsum("ijkl,ijnm -> iknlm", K, K.conj()).reshape((d, r, r))
    rho = randpsd(r).copy()
    A = randKrausSet_Haar(1, r, n_povm)[0].conj()
    E = np.array([(A[i].T.conj() @ A[i]).reshape(-1) for i in range(n_povm)]).copy()
    return K, X, E, rho


def perturbed_target_init(X_target: np.ndarray, rK: int) -> np.ndarray:
    """Generates a small random noise gate around the identity and applies it to the target gate.

    The reason for using this gate as an initialization as opposed the target gate itself, is
    that the non-dominant Kraus operators we start with are now not zero, but small random matrices.
    Observations show that with this start, the non-dominant Kraus operators converge faster.

    Args:
        X_target: Current gate estimate
        rK: Number of Kraus operators per gate ("Kraus rank") for the initialization

    Returns:
        K_init: Each subarray along the first axis contains a set of Kraus operators.
            The second axis enumerates Kraus operators for a gate specified by the first axis.

    """
    d, r, _ = X_target.shape
    pdim = int(np.sqrt(r))
    K_perturb = randKrausSet(d, r, rK, a=0.05)
    X_perturb = np.einsum("ijkl,ijnm -> iknlm", K_perturb, K_perturb.conj()).reshape((d, r, r))
    X_init = np.einsum("ikl,ilm ->ikm", X_perturb, X_target)
    K_init = Kraus_rep(X_init, d, pdim, rK)
    return K_init


def basis(size: int, index: int) -> np.ndarray:
    """Creates standard basis vectors.

    Args:
        size: Vector space dimension
        index: Index of basis vector

    Returns:
        vec: Vector with entry 1 at position given by index and zeros elsewhere

    """
    vec = np.zeros(size)
    vec[index] = 1.0
    return vec


def depol(pdim: int, p: float) -> np.ndarray:
    """Kraus representation of depolarizing channel.

    Args:
        pdim: Physical dimension
        p: Error probability

    Returns:
        K_depol: Each entry along the first axis contains a set of Kraus operators. The second axis enumerates
            Kraus operators for a gate specified by the first axis.

    Notes:
        The depolarizing channel is defined as L(rho) = (1-p)*rho + p/pdim*Id.

    """
    phi_plus = np.sum([np.kron(basis(pdim, i), basis(pdim, i)) for i in range(pdim)], axis=0)
    choi_state = p / pdim * np.eye(pdim**2) + (1 - p) * np.kron(phi_plus, phi_plus.reshape(pdim**2, 1))
    K_depol = la.cholesky(choi_state)
    return K_depol.reshape(pdim, pdim, pdim**2).swapaxes(0, 2)


def batch(y: np.ndarray, J: np.ndarray, bsize: int) -> tuple[np.ndarray, np.ndarray]:
    """Returns random batch of sequences and corresponding measurements.

    Args:
        y: 2D array of measurement outcomes for sequences in J; Each column contains the outcome probabilities for
            a fixed sequence
        J: 2D array where each row contains the gate indices of a gate sequence
        bsize: Size of the batch (number of sequences)

    Returns:
        y_b: Randomly subsampled columns of y
        J_b: Randomly subsampled rows of J in accordance the the columns selected in y_b

    """
    if y.shape[1] <= bsize:
        return y, J
    if bsize < 1:  # if batch size is given as ratio
        bsize = int(bsize * len(J) // 1)
    batchmask = np.array([1] * bsize + [0] * (len(J) - bsize))
    np.random.shuffle(batchmask)
    J_b = J[batchmask == 1]
    y_b = y[:, batchmask == 1]
    return y_b, J_b


def F_avg_X(X: np.ndarray, K: np.ndarray) -> tuple[float, np.ndarray]:
    r"""Returns the average gate fidelity between a superoperator and a set of Kraus operators.

    Args:
        X: CPT superoperator of size (pysical dimension ** 2) x (pysical dimension ** 2) as 2D array.
        K: Array of Kraus operators with size (Kraus rank) x (pysical dimension) x (pysical dimension) as 3D array.

    Returns:
        Average gate fidelity.

    Notes:
        No gauge optimization is involved; average gate fidelity is gauge dependent.

    """
    pdim = K.shape[2]
    d = K.shape[0]
    Fid_list = []
    for k in range(d):
        choi_inner_prod = np.einsum("imjl,pml,pij", X[k].reshape(pdim, pdim, pdim, pdim), K[k], K[k].conj())

        unitality_term = np.einsum("imkk,pml,pil", X[k].reshape(pdim, pdim, pdim, pdim), K[k], K[k].conj())
        Fid = (choi_inner_prod + unitality_term) / pdim / (pdim + 1)
        Fid_list.append(Fid)
    return np.average(np.real(Fid_list)), np.real(Fid_list)


def MVE(
    gates_true: list[np.ndarray],
    gates_estimate: list[np.ndarray],
    d: int,
    length: int,
    n_povm: int,
    samples: int = 10000,
) -> tuple[float, float]:
    """Mean varation error between the outputs of two gate sets on random sequences.

    Args:
        gates_true: Target gates, POVM and initial state
        gates_estimate: Current gate, POVM and initial state estimate
        d: Number of different gates in the gate set
        length: Length of the test sequences
        n_povm: Number of POVM elements
        samples: Number of random gate sequences over which the mean variation error is computed

    Returns:
        MVE: Mean varaition error

    Notes:
        Sequences are drawn without replacement from initally d**l possibilities.
        For each sequence the total variation error of the two probability distribution
        over the POVM elements is computed. Afterwards the meean over these total
        variation errors is returned.

    """
    X_true, E_true, rho_true = gates_true
    X, E, rho = gates_estimate
    if samples == "all" or np.log(samples) / np.log(d) > length:
        J = np.random.randint(0, d, length * d**length).reshape(d**length, length)
    else:
        J = np.random.randint(0, d, length * samples).reshape(samples, length)
    return MVE_lower(X_true, E_true, rho_true, X, E, rho, J, n_povm)


